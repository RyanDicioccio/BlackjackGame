
public class Cards {
	
	//delcare suit and rank vrb
	public String suit;
	public String rank;
	
	//overloaded constuctor
	public Cards(String suit, String rank) {
		this.suit = suit;
		this.rank = rank; 
	}

	
	//getter method for the suit
	public String getSuit(){
		return suit;

	}

	//getter method of the rank of the card
	public  String getRank(){
		return rank;

	}
	
	





}
